//Разбивка на подзадачи:
//1. Создать метод, который создаёт и заполняет массив положительными трёхзначгыми числами.
//2. Создать метод, который определяет чётность числа в массиве и суммирует их количество.
//3. Создать метод, который печатает массив в консоль.
//4. Создать метод, который печататет количество чётных чисел в массиве.
